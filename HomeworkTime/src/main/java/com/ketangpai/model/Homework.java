public class Homework {

	private int id;// homework auto increment id

	private int uid; // id who created the homework

	private int courseid;// courses' id

	private String title;// courses' title

	private String description;// courese

	private long endtime;// homework's endtime

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public int getCourseid() {
		return courseid;
	}

	public void setCourseid(int courseid) {
		this.courseid = courseid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public long getEndtime() {
		return endtime;
	}

	public void setEndtime(long endtime) {
		this.endtime = endtime;
	}

	@Override
	public String toString() {
		return "Homework [id=" + id + ", uid=" + uid + ", courseid=" + courseid
				+ ", title=" + title + ", description=" + description
				+ ", endtime=" + endtime + "]";
	}
}
